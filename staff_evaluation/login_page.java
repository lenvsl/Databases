    private void jTextField_UsernameFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField_UsernameFocusGained
                                              
        // clear the textfield on focus if the text is "username"
        if(jTextField_Username.getText().trim().toLowerCase().equals("username"))
        {
            jTextField_Username.setText("");
            jTextField_Username.setForeground(Color.black);
        }
        
    }//GEN-LAST:event_jTextField_UsernameFocusGained

    private void jTextField_UsernameFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextField_UsernameFocusLost
                             
        // if the text field is equal to username or empty
        // we will set the "username" text in the field 
        // on focus lost event
        
        if(jTextField_Username.getText().trim().equals("") || 
           jTextField_Username.getText().trim().toLowerCase().equals("username"))
        {
            jTextField_Username.setText("username");
            jTextField_Username.setForeground(new Color(153,153,153));
        }
    }//GEN-LAST:event_jTextField_UsernameFocusLost

    private void jPasswordFieldFocusGained(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jPasswordFieldFocusGained
                                                  
        // clear the password on focus if the text is "password"
        String pass = String.valueOf(jPasswordField.getPassword());
        if(pass.trim().toLowerCase().equals("password"))
        {
            jTextField_Username.setText("");
            jTextField_Username.setForeground(Color.black);
        }
        
    }//GEN-LAST:event_jPasswordFieldFocusGained

    private void jPasswordFieldFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jPasswordFieldFocusLost
                           
        // if the pass  field is equal to password or empty
        // we will set the "password" text in the field 
        // on focus lost event
         String pass = String.valueOf(jPasswordField.getPassword());
        if(pass.trim().equals("") || 
           pass.trim().toLowerCase().equals("password"))
        {
            jPasswordField.setText("password");
            jPasswordField.setForeground(new Color(153,153,153));
        }
    }//GEN-LAST:event_jPasswordFieldFocusLost

    private void jButton_LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LoginActionPerformed
        Connection myConn = null;
        PreparedStatement st = null;
        ResultSet rs ;
       
        // get the username & password &option
        String username = jTextField_Username.getText();
        String password = String.valueOf(jPasswordField.getPassword());
        String option = String.valueOf(jComboBox1.getSelectedItem());
        
        //create a select query to check if the username and the password exist in the database
        String query = "SELECT * FROM `user` WHERE `user_username` = ? AND `user_password` = ? AND `option` = ?";
     
        // show a message if the username or the password fields are empty
        if(username.trim().equals("username"))
        {
            JOptionPane.showMessageDialog(null, "Enter Your Username", "Empty Username", 2);
        }
        else if (password.trim().equals("password"))
        {
            JOptionPane.showMessageDialog(null, "Enter Your Password", "Empty Password", 2);
        }
        else if (option.trim().equals("Select"))
        {
            JOptionPane.showMessageDialog(null, "Select  type of user", "error",  3);
        }
        else{
            try {
            myConn = DriverManager.getConnection("jdbc:mysql://localhost/staffevaluation", "root" , "");
            st=myConn.prepareStatement(query);
            


            st.setString(1, username);
            st.setString(2, password);
            st.setString(3, option);
            rs = st.executeQuery();
            
            if(rs.next())
            {
              String s1 = rs.getString("option");  
              if(option.equalsIgnoreCase("Employee")&& s1.equalsIgnoreCase("employee"))
              {    
                 // CloseMe();
                  //show a new form
                  EmployeePage em = new EmployeePage();
                  EmployeePage.jLabel1.setText(jTextField_Username.getText());
                  em.setVisible(true);
                  em.pack();
                  em.setLocationRelativeTo(null);
                  //close the current form
                  this.dispose();
                  setVisible(false);
              }
              if(option.equalsIgnoreCase("Administrator")&& s1.equalsIgnoreCase("administrator")){
                  //CloseMe();
                  AdminPage ad = new AdminPage();
                  AdminPage.jLabel1_Admin.setText("Welcome " + jTextField_Username.getText());
                  ad.setVisible(true); 
                 
                  ad.pack();
                  ad.setLocationRelativeTo(null);
                   setVisible(false);
                 
                  //close the current form
                  //this.dispose();
              }
              if(option.equalsIgnoreCase("Manager")&& s1.equalsIgnoreCase("manager")){
                 // CloseMe();
                 String username_manager = jTextField_Username.getText();
                 ManagerPage ma = new ManagerPage();
                 ManagerPage.jLabel1_manager.setText("WELCOME " + jTextField_Username.getText());
                 Manager_Company mc = new Manager_Company();
                 Manager_Company.jLabel_company.setText(username_manager);
                  //new Manager_Company(username_manager).setVisible(true);
                  ma.setVisible(true);
                  ma.pack();
                  ma.setLocationRelativeTo(null);
                   setVisible(false);
              }
              if(option.equalsIgnoreCase("Evaluator")&& s1.equalsIgnoreCase("evaluator")){
                  String username_manager = jTextField_Username.getText();
                  EvaluatorPage ev = new EvaluatorPage();
                  EvaluatorPage.jLabel3.setText( jTextField_Username.getText());
                   //Manager_Company mc = new Manager_Company();
                  ev.setVisible(true);
                 
                  ev.pack();
                  ev.setLocationRelativeTo(null);
                   setVisible(false);
              }
           
              
              /*JOptionPane.showMessageDialog(null, "Hallo","Welcome to the database staffevaluation!",2);*/
            }else{
                // error message
                JOptionPane.showMessageDialog(null, "Invalid Username / Password /User","Login Error",2);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(login_page.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        
        }
          
    }//GEN-LAST:event_jButton_LoginActionPerformed

